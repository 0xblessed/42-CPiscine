#include <unistd.h>

void	ft_print_alphabet(void)
{
	char	a;
	char	z;
	char	i;

	a = 'a';
	z = 'z';
	i = a;
	while (i <= z)
	{
		write(1, &i, 1);
		i++;
	}
}
